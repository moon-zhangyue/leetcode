class Solution
{

    /**
     * @param Integer[] $nums
     * @param Integer   $k
     *
     * @return NULL
     */
    function rotate(&$nums, $k)
    {
        $arr = [];
        $len = count($nums);
        for ($i = $len - 1; $i >= 0; $i--) {
            if ($i + $k >= $len) {
                $arr[$i + $k - $len] = $nums[$i];
            } else {
                $arr[$i + $k] = $nums[$i];
            }
        }
        ksort($arr);
        return $arr;
    }
}
