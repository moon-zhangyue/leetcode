class Solution
{

    /**
     * @param Integer[] $prices
     *
     * @return Integer
     */
    function maxProfit($prices)
    {
        //ä¸€ æš´åŠ›æ³• è¶…æ—¶
        $max = 0;

        for ($i = 0; $i < count($prices); $i++) {
            for ($j = $i + 1; $j < count($prices); $j++) {
                if ($prices[$j] > $prices[$i]) {
                    $a   = $prices[$j] - $prices[$i];
                    $max = $max > $a ? $max : $a;
                }
            }
        }

        return $max;
    }
}
